package dopFacade;

public class Car {
    public void onСar() {
        System.out.println("Машина заведена ");
    }
    public void onFara() {
        System.out.println("Фары включены");
    }
    public void offCar() {
        System.out.println("Машина заглушена");
    }
    public void offFara() {
        System.out.println("Фары выключены");
    }
}
